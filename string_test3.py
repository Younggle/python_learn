## -----------------------String Methods ---------------

letter_b = 'b'
num_3 = '3'
space_i = ' '

print("Is letter_b is a letter or number:",letter_b.isalnum())

print("Is 'b' is a letter:",letter_b.isalpha())

print("Is 3 is a number:",num_3.isdigit())

print("Is 'b' is a lowercase:",letter_b.islower())

print("Is 'b' is uppercase:",letter_b.isupper())

print("Is ' ' is a space:",space_i.isspace())
